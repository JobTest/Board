package com.pb.dashboard.vitrina.core.types;

public enum Period {

    H24(24, "24:00", "23:00-24:00"),
    H23(23, "23:00", "22:00-23:00"),
    H22(22, "22:00", "21:00-22:00"),
    H21(21, "21:00", "20:00-21:00"),
    H20(20, "20:00", "19:00-20:00"),
    H19(19, "19:00", "18:00-19:00"),
    H18(18, "18:00", "17:00-18:00"),
    H17(17, "17:00", "16:00-17:00"),
    H16(16, "16:00", "15:00-16:00"),
    H15(15, "15:00", "14:00-15:00"),
    H14(14, "14:00", "13:00-14:00"),
    H13(13, "13:00", "12:00-13:00"),
    H12(12, "12:00", "11:00-12:00"),
    H11(11, "11:00", "10:00-11:00"),
    H10(10, "10:00", "9:00-10:00"),
    H9(9, "9:00", "8:00-9:00"),
    H8(8, "8:00", "7:00-8:00"),
    H7(7, "7:00", "6:00-7:00"),
    H6(6, "6:00", "5:00-6:00"),
    H5(5, "5:00", "4:00-5:00"),
    H4(4, "4:00", "3:00-4:00"),
    H3(3, "3:00", "2:00-3:00"),
    H2(2, "2:00", "1:00-2:00"),
    H1(1, "1:00", "0:00-1:00");

    private int id;
    private String hour;
    private String period;

    private Period(int id, String hour, String period) {
        this.id = id;
        this.hour = hour;
        this.period = period;
    }

    public int getId() {
        return id;
    }

    public String getHour() {
        return hour;
    }

    public String getPeriod() {
        return period;
    }
}
